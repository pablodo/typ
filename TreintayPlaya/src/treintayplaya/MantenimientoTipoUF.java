    private void jbtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnOKActionPerformed
        try {
            java.sql.PreparedStatement pstm = cnx.prepareStatement("insert into TiposUF (tufDetalle, tufMetros2, tufAmbientes, tufUbicacion) values (?, ?, ?, ?)");
            
            pstm.setString(1, jtxfTUF.getText());
            pstm.setString(2, jftfM2.getText());
            pstm.setString(3, jcbxAmbientes.getSelectedItem().toString());
            pstm.setString(4, jtxfUbicacion.getText());
            
            int result = pstm.executeUpdate();
            
            if (result == 1) {
                Object [] fila = {jtxfTUF.getText(), jftfM2.getText(), jcbxAmbientes.getSelectedItem().toString(), jtxfUbicacion.getText()};
                
                modelo.addRow(fila);
            }
            
            jpnlAltaTUF.setVisible(false);
            
        } catch (java.sql.SQLException sqle) {
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_jbtnOKActionPerformed

    public void cargaTabla() {
        try {

            java.sql.Statement stm = cnx.createStatement();

            java.sql.ResultSet rst = stm.executeQuery("select tufDetalle, tufMetros2, tufAmbientes, tufUbicacion from TiposUF order by tufDetalle");

            while(rst.next()) {

                Object [] fila = new Object[4];

                for(int i = 0; i < 4; i++)
                    fila[i] = rst.getObject(i + 1);

                modelo.addRow(fila);
            }
        } catch (java.sql.SQLException sqle) {
            sqle.printStackTrace();
        }
    }
    
