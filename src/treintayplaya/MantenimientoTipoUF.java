    private void jbtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnOKActionPerformed
        try {
            String query = "insert into TiposUF (tufDetalle, tufMetros2, tufAmbientes, tufUbicacion) values (?, ?, ?, ?)";
            if (actualizar)
                query = "UPDATE TiposUF SET tufDetalle=?, tufMetros2=?, tufAmbientes=?, tufUbicacion=? WHERE tufID=?";
            
            java.sql.PreparedStatement pstm = cnx.prepareStatement(query);
            
            pstm.setString(1, jtxfTUF.getText());
            pstm.setInt   (2, Integer.valueOf(jftfM2.getText()));
            pstm.setInt   (3, Integer.valueOf(jcbxAmbientes.getSelectedItem().toString()));
            pstm.setString(4, jtxfUbicacion.getText());
            if (actualizar)
                pstm.setInt(5, ids.get(jtblTUF.getSelectedRow()));
            
            int result = pstm.executeUpdate();
            
            if (result == 1) {
                cargaTabla();
            }
            pstm.close();
            altaUF(false);
            
        } catch (java.sql.SQLException sqle) {
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_jbtnOKActionPerformed

    private void jbtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBorrarActionPerformed
        try {
            String query = "DELETE FROM TiposUF WHERE tufID = ?";
            java.sql.PreparedStatement pstm = cnx.prepareCall(query);
            pstm.setInt(1, ids.get(jtblTUF.getSelectedRow()));
            pstm.execute();
            cargaTabla();
            pstm.close();
        } catch (SQLException ex) {
            Logger.getLogger(MantenimientoTipoUF.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnBorrarActionPerformed

    private void jbtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnActualizarActionPerformed
        Integer row = jtblTUF.getSelectedRow();
        if (row < 0){
            return;
        }
        actualizar = true;
        altaUF(true);
        jtxfTUF.setText(jtblTUF.getValueAt(row, 0).toString());
        jftfM2.setText(jtblTUF.getValueAt(row, 1).toString());
        jcbxAmbientes.setSelectedItem(jtblTUF.getValueAt(row, 2).toString());
        jtxfUbicacion.setText(jtblTUF.getValueAt(row, 3).toString());
    }//GEN-LAST:event_jbtnActualizarActionPerformed

    public void cargaTabla() {
        try {
            java.sql.Statement stm = cnx.createStatement();

            java.sql.ResultSet rst = stm.executeQuery("select tufID, tufDetalle, tufMetros2, tufAmbientes, tufUbicacion from TiposUF order by tufDetalle");
            
            ids = new ArrayList<Integer>();
            modelo = new javax.swing.table.DefaultTableModel();
            jtblTUF.setModel(modelo);
            Object[] headers = {"Tipo de Unidad Funcional", 
                                "M2.",
                                "Ambientes",
                                "Ubicación"};
            modelo.setColumnIdentifiers(headers);
            while(rst.next()) {
                ids.add(rst.getInt("tufID"));
                Object [] fila = {rst.getString("tufDetalle"),
                                  rst.getInt("tufMetros2"),
                                  rst.getInt("tufAmbientes"),
                                  rst.getString("tufUbicacion")
                                 };

                modelo.addRow(fila);
            }
            
            rst.close();
            stm.close();
        } catch (java.sql.SQLException sqle) {
            sqle.printStackTrace();
        }
    }
    
    private void altaUF(boolean option){
        jtxfTUF.setEnabled(option);
        jtxfUbicacion.setEnabled(option);
        jcbxAmbientes.setEnabled(option);
        jbtnOK.setEnabled(option);
        jftfM2.setEnabled(option);
        jtblTUF.setEnabled(! option);
        jtxfTUF.setText("");
        jftfM2.setText("");
        jtxfUbicacion.setText("");
        jcbxAmbientes.setSelectedIndex(-1);
    }
    
