	public DialogSelector(Frame parent, boolean modal, String query, String campoID, String campoNombre){
		this(parent, modal);
		DefaultListModel model = new DefaultListModel();
		lista.setModel(model);
		java.sql.Connection cnx = Conexion.getInstance().getConnection();
		try {
			java.sql.PreparedStatement pstm = cnx.prepareStatement(query);
			java.sql.ResultSet rst = pstm.executeQuery();
			while(rst.next()){
				ids.add(rst.getInt(campoID));
				model.addElement(rst.getString(campoNombre));
			}
			rst.close();
			pstm.close();
		} catch (SQLException ex) {
			Logger.getLogger(DialogSelector.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

