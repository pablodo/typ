	public static Integer seleccionar(String title, String query, String campoID, String campoNombre){
		DialogSelector dialog = new DialogSelector(new Frame(), true, query, campoID, campoNombre);
		dialog.setTitle(title);
		if (dialog.ids.size() ==1){
			return dialog.ids.get(0);
		}
		dialog.setVisible(true);
		return dialog.getSelected();
	}

	private int getSelected() {
		if (ids.isEmpty())
			return 0;
		int selected = lista.getSelectedIndex();
		if (selected < 0) selected = 0;
		return ids.get(selected);
	}
